interface ResourceTreeNode {
  key: string;
  title: string;
  vertexId: string;
  parentId?: string;
  value: GetHttpsResourcePartialFragment;
  appliedConversionMethod?: ToTreeVertexAppliedConversionMethodPartialFragment;
  children?: ResourceTreeNode[];
}

const transformToTree = (resourceTree: DataPartialFragment["resourceTree"]) => {
  const { root, nonRootVertices } = resourceTree;
  const childrenMap = new Map<
    string,
    DataPartialFragment["resourceTree"]["nonRootVertices"][0][]
  >();
  nonRootVertices.forEach((vertex) => {
    const parentId = vertex.parentId;
    if (!childrenMap.has(parentId)) {
      childrenMap.set(parentId, []);
    }
    childrenMap.get(parentId)?.push(vertex);
  });
  const buildBranch = (
    node:
      | DataPartialFragment["resourceTree"]["nonRootVertices"][0]
      | DataPartialFragment["resourceTree"]["root"],
  ): ResourceTreeNode => {
    const children = childrenMap.get(node.vertexId) || [];
    return {
      ...node,
      key: node.vertexId,
      title: `${node.value.dataFormat?.name ?? node.value.dataFormatId} (vertex ${node.vertexId})`,
      // node.value.locator || node.value.description || `ID: ${node.vertexId}`,
      children: children.map((child) => buildBranch(child)),
    };
  };
  return buildBranch(root);
};

const getBreadcrumbItems = (
  selectedId: string,
  flatNodes: (
    | DataPartialFragment["resourceTree"]["nonRootVertices"][0]
    | DataPartialFragment["resourceTree"]["root"]
  )[],
) => {
  const path = [];
  let currentId = selectedId;
  while (currentId) {
    const node = flatNodes.find((node) => node.vertexId === currentId);
    if (!node) break;
    path.unshift({
      title: `${node.value.dataFormat?.name ?? node.value.dataFormatId} (${node.vertexId})`,
    });
    if (node.__typename === "GetHttpsResourceTreeNonRootVertex") {
      currentId = node.parentId;
    } else {
      break;
    }
  }
  return path;
};

const nameFallback = (id: Scalars["Uuid"]["output"]) => ({
  uuid: id,
  name: id,
});

