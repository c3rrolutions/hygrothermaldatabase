import { IncomingMessage, ServerResponse } from "http";
import { useMemo } from "react";
import {
  ApolloClient,
  InMemoryCache,
  HttpLink,
  ErrorLike,
} from "@apollo/client";
import { Defer20220824Handler } from "@apollo/client/incremental";
import { LocalState } from "@apollo/client/local-state";
import { CombinedGraphQLErrors } from "@apollo/client/errors";
import { PersistedQueryLink } from "@apollo/client/link/persisted-queries";
import merge from "deepmerge";
import { sha256 } from "crypto-hash";
import fragmentMatcherTypes from "../__generated__/fragmentMatcherTypes"; // generated by `fragment-matcher` plugin

let apolloClient: ApolloClient | undefined;

export type ResolverContext = {
  req?: IncomingMessage;
  res?: ServerResponse;
};

function getCookie(name: string, cookies: string): string | null {
  return (
    cookies
      .split(";")
      .map((cookie) => cookie.trim())
      .filter((cookie) => {
        return cookie.substring(0, name.length + 1) === `${name}=`;
      })
      .map((cookie) => {
        return decodeURIComponent(cookie.substring(name.length + 1));
      })[0] || null
  );
}

export function getXsrfToken(): string | null {
  return getCookie("XSRF-TOKEN", document.cookie);
}

function createIsomorphLink(context: ResolverContext = {}) {
  var headers: Record<string, string> = {};
  headers["accept"] = "application/json";
  var cookie = context.req?.headers?.cookie;
  if (cookie) {
    headers["cookie"] = cookie;
  }
  if (typeof window !== "undefined") {
    var antiforgeryToken = getXsrfToken();
    if (antiforgeryToken) headers["X-XSRF-TOKEN"] = antiforgeryToken;
  }
  return new PersistedQueryLink({
    sha256,
    useGETForHashedQueries: false,
  }).concat(
    new HttpLink({
      uri:
        // In the case `typeof window === "undefined"`, that is on the server
        // side, because we require HTTPS for the JWT bearer token, we cannot use
        // "http://backend:8080/graphql/" to make local requests. When the reverse
        // proxy NGINX serves the certificate like in development we could use
        // "https://nginx:443/graphql/" to make local requests, which this does
        // however not work in production. Thus, on the server side we also use
        // `${process.env.NEXT_PUBLIC_DATABASE_URL}/graphql/` as on the client
        // side.
        typeof window === "undefined"
          ? new URL("/graphql/", process.env.NEXT_PUBLIC_DATABASE_URL).href
          : new URL("/graphql/", process.env.NEXT_PUBLIC_DATABASE_URL).href,
      useGETForQueries: false, // Use `POST` for queries to avoid "414 Request-URI Too Large" errors
      credentials: "same-origin",
      headers: headers,
    }),
  );
}

function createApolloClient(context?: ResolverContext) {
  return new ApolloClient({
    ssrMode: typeof window === "undefined",
    link: createIsomorphLink(context),
    cache: new InMemoryCache({
      possibleTypes: fragmentMatcherTypes.possibleTypes,
    }),
    localState: new LocalState({}),
    incrementalHandler: new Defer20220824Handler(),
  });
}

export function initializeApollo(
  initialState: any = null,
  // Pages with Next.js data fetching methods, like `getStaticProps`, can send
  // a custom context which will be used by `SchemaLink` to server render pages
  context?: ResolverContext,
) {
  const _apolloClient = apolloClient ?? createApolloClient(context);

  // If your page has Next.js data fetching methods that use Apollo Client, the initial state
  // get hydrated here
  if (initialState) {
    // Get existing cache, loaded during client side data fetching
    const existingCache = _apolloClient.extract();
    // Merge the existing cache into data passed from getStaticProps/getServerSideProps
    const data = merge(initialState, existingCache as any);
    // Restore the cache with the merged data
    _apolloClient.cache.restore(data);
  }
  // For SSG and SSR always create a new Apollo Client
  if (typeof window === "undefined") return _apolloClient;
  // Create the Apollo Client once in the client
  if (!apolloClient) apolloClient = _apolloClient;

  return _apolloClient;
}

// See https://www.apollographql.com/docs/react/data/error-handling
export function stringifyApolloError(error: ErrorLike) {
  if (CombinedGraphQLErrors.is(error)) {
    return `Name(${error.name}); Message(${error.message}); Extensions(${
      error.extensions
    }); Stack(${error.stack}); GraphQL Errors(${error.errors
      .map(
        (e) =>
          `[Message(${e.message}); Path(${e.path?.join(
            ".",
          )}); Locations(${e.locations?.join(", ")}); Extensions(${
            e.extensions
          })]`,
      )
      .join(", ")})`;
    // } if (CombinedProtocolErrors.is(error)) {
    // Handle multipart subscription protocol errors
    // } if (LocalStateError.is(error)) {
    // Handle errors thrown by the `LocalState` class
    // } if (ServerError.is(error)) {
    // Handle server HTTP errors
    // } if (ServerParseError.is(error)) {
    // Handle JSON parse errors
    // } if (UnconventionalError.is(error)) {
    // Handle errors thrown by irregular types
  }
  return `Name(${error.name}); Message(${error.message}); Stack(${error.stack})`;
}

export function useApollo(initialState: any) {
  const store = useMemo(() => initializeApollo(initialState), [initialState]);
  return store;
}

